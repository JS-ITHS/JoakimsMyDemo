public class BigClass {
    private int number = 0;
    private String text = null;

    public BigClass() {

    }
    public BigClass(int number) {
        this.number = number;
    }
    public BigClass(String text) {
        this.text = text;
    }
    public BigClass(int number,String text) {
        this.number = number;
        this.text = text;
    }

}
